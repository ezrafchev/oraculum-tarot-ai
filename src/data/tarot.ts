function clampPolarity(value: number): -2 | -1 | 0 | 1 | 2 {
  return Math.max(-2, Math.min(2, value)) as -2 | -1 | 0 | 1 | 2;
}

const majorCards: TarotCardData[] = majors.map((card) => ({
  ...card,
  id: `major-${String(card.number).padStart(2, "0")}`,
  arcana: "major",
}));

const minorCards: TarotCardData[] = (
  Object.keys(suitSeeds) as Suit[]
).flatMap((suit) => {
  const suitSeed = suitSeeds[suit];
  return rankSeeds.map((rank) => ({
    id: `${suit.toLowerCase()}-${String(rank.number).padStart(2, "0")}`,
    name: `${rank.label} de ${suit}`,
    number: rank.number,
    arcana: "minor" as const,
    suit,
    element: suitSeed.element,
    astrology: `${suitSeed.element} · ciclo ${rank.number}`,
    keywordsUpright: [...rank.upright, suitSeed.domain.split(",")[0]],
    keywordsReversed: [...rank.reversed, suitSeed.shadow],
    meanings: {
      general: `No campo de ${suitSeed.domain}, esta carta representa ${rank.essence}.`,
      affective: `Em relações, articula ${suitSeed.affective} com ${rank.essence}.`,
      professional: `No trabalho, combina ${suitSeed.professional} com ${rank.essence}.`,
      financial: `Em finanças, relaciona ${suitSeed.financial} a ${rank.essence}.`,
      spiritual: `No plano interior, aproxima ${suitSeed.spiritual} de ${rank.essence}.`,
    },
    advice: `${rank.advice}; aplique isso ao campo de ${suitSeed.domain}.`,
    warning: `${rank.warning}; observe também ${suitSeed.shadow}.`,
    light: `${suitSeed.light} em expressão ${rank.label.toLowerCase()}`,
    shadow: `${suitSeed.shadow} quando o padrão se desequilibra`,
    visualDescription: `${rank.label} símbolos de ${suit} organizados em uma cena ligada a ${suitSeed.domain}.`,
    relations: [
      `Cartas de ${suit} reforçam ${suitSeed.domain}`,
      `Outras cartas de número ${rank.number} repetem o estágio ${rank.label}`,
    ],
    categories: [...rank.categories, suit === "Copas" ? "vínculo" : suit === "Espadas" ? "dúvida" : "clareza"],
    polarity: clampPolarity(rank.polarity + suitSeed.polarityAdjustment),
    symbol: suitSeed.symbol,
  }));
});

export const TAROT_DECK: TarotCardData[] = [...majorCards, ...minorCards];

export const TAROT_ENGINE_VERSION = "1.0.0";

export function getCardById(id: string): TarotCardData | undefined {
  return TAROT_DECK.find((card) => card.id === id);
}

